'''
Sort(); altera uma lista
Sorted() = significado classificado
Sempre retorna uma lista com os iteraveis organizados
pode organizar crescente ou decrescenteo com a funcao reverse= True

Sorted() =  Nao altera o dado inicial apenas organiza/classifica
'''

n = [1, 3, 0, 6, 7, 2, 123, 56, 278, 99]
print(n)
print(sorted(n))
print(sorted(n, reverse=True))
